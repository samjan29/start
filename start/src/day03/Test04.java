package day03;

public class Test04 {
	public static void main(String[] args) {
		int no1 = 5;
		int no2 = 3;
		
		System.out.println("no1 / no2 = " + (no1 / no2));		// 나누기 계산
		
		System.out.println("no1 + no2 = " + (no1 + no2));		// 더하기 계산
		System.out.println("no1 - no2 = " + (no1 - no2));		// 빼기 계산
		System.out.println("no1 * no2 = " + (no1 * no2));		// 곱하기 계산
		System.out.println("no1 / no2 = " + (no1 / no2));		// 나누기 계산
		System.out.println("no1 % no2 = " + (no1 % no2));		// 나머지 계산
	}
}